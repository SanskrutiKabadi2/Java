import java.util.*;
class Operators14{
        public static void main(String[] args){
                Scanner Sc=new Scanner(System.in);
                int A,B;
                System.out.println("enter first number :");
                A=Sc.nextInt();
                System.out.println("enter second number :");
                B=Sc.nextInt();
                System.out.println(A++);
                System.out.println(A++);
                System.out.println(A--);
                System.out.println(B--);
                System.out.println(A);
                System.out.println(B);
        }

}
